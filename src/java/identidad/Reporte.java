package identidad;



public class Reporte 
{
     int idp, idcat;
     String des;
     public Reporte(){
        idp=0;
        idcat=0;
    }

    public int getIdp() {
        return idp;
    }

    public void setIdp(int idp) {
        this.idp = idp;
    }

    public int getIdcat() {
        return idcat;
    }

    public void setIdcat(int idcat) {
        this.idcat = idcat;
    }

    public String getDes() {
        return des;
    }

    public void setDes(String des) {
        this.des = des;
    }
     
     
     
}

