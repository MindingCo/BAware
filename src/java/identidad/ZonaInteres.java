package identidad;

public class ZonaInteres
{
    Float cx1, cy1, cx2, cy2;
    int id;

    public Float getCx1() 
    {
        return cx1;
    }

    public void setCx1(Float cx1) 
    {
        this.cx1 = cx1;
    }

    public Float getCy1() 
    {
        return cy1;
    }

    public void setCy1(Float cy1) 
    {
        this.cy1 = cy1;
    }

    public Float getCx2() 
    {
        return cx2;
    }

    public void setCx2(Float cx2) 
    {
        this.cx2 = cx2;
    }

    public Float getCy2() 
    {
        return cy2;
    }

    public void setCy2(Float cy2) 
    {
        this.cy2 = cy2;
    }

    public int getId() 
    {
        return id;
    }

    public void setId(int id) 
    {
        this.id = id;
    }
    
    
}
